// Eye Toggle function
import { useState } from "react";

export const usePasswordToggle = () => {
  const [visible, setVisible] = useState(false);

  const toggleVisibility = () => {
    setVisible(!visible);
  };

  return {
    inputType: visible ? "text" : "password",
    toggleVisibility,
    isVisible: visible,
  };
};
